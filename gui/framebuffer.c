/* -- Text rendering (with full bounds checking) ------------------------ */

void fb_putchar(int32_t x, int32_t y, char c, uint32_t fg, uint32_t bg) {
    if (!fb_is_valid()) return;
    if (c < 32 || c > 126) c = '?';

    /* Quick reject if entirely off-screen */
    if (x + FONT_W <= 0 || y + FONT_H <= 0) return;
    if (x >= (int32_t)fb.width || y >= (int32_t)fb.height) return;

    const uint8_t *glyph = font8x16[c - 32];
    for (int row = 0; row < FONT_H; row++) {
        int32_t py = y + row;
        if (py < 0 || (uint32_t)py >= fb.height) continue;
        uint8_t bits = glyph[row];
        uint32_t *rp = (uint32_t*)((uint8_t*)fb.addr + py * fb.pitch);
        for (int col = 0; col < FONT_W; col++) {
            int32_t px = x + col;
            if (px >= 0 && (uint32_t)px < fb.width)
                rp[px] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}

void fb_puts(int32_t x, int32_t y, const char *s, uint32_t fg, uint32_t bg) {
    if (!fb_is_valid()) return;
    if (!s) return;

    int32_t cx = x;
    while (*s) {
        if (*s == '\n') {
            cx = x;
            y += FONT_H;
        } else {
            if (y >= (int32_t)fb.height) return;
            if (cx < (int32_t)fb.width)
                fb_putchar(cx, y, *s, fg, bg);
            cx += FONT_W;
        }
        s++;
    }
}

/* -- Transparent-background text (foreground pixels only) -------------
 * These variants only draw the glyph foreground pixels and leave the
 * background untouched.  Use when rendering text over a gradient or
 * image where a flat bg color would create visible banding artefacts. */

void fb_putchar_nobg(int32_t x, int32_t y, char c, uint32_t fg) {
    if (!fb_is_valid()) return;
    if (c < 32 || c > 126) c = '?';
    if (x + FONT_W <= 0 || y + FONT_H <= 0) return;
    if (x >= (int32_t)fb.width || y >= (int32_t)fb.height) return;

    const uint8_t *glyph = font8x16[c - 32];
    for (int row = 0; row < FONT_H; row++) {
        int32_t py = y + row;
        if (py < 0 || (uint32_t)py >= fb.height) continue;
        uint8_t bits = glyph[row];
        if (!bits) continue;  /* skip blank rows for speed */
        uint32_t *rp = (uint32_t*)((uint8_t*)fb.addr + py * fb.pitch);
        for (int col = 0; col < FONT_W; col++) {
            if (bits & (0x80 >> col)) {
                int32_t px = x + col;
                if (px >= 0 && (uint32_t)px < fb.width)
                    rp[px] = fg;
            }
        }
    }
}

void fb_puts_nobg(int32_t x, int32_t y, const char *s, uint32_t fg) {
    if (!fb_is_valid()) return;
    if (!s) return;

    int32_t cx = x;
    while (*s) {
        if (*s == '\n') {
            cx = x;
            y += FONT_H;
        } else {
            if (y >= (int32_t)fb.height) return;
            if (cx < (int32_t)fb.width)
                fb_putchar_nobg(cx, y, *s, fg);
            cx += FONT_W;
        }
        s++;
    }
}

int fb_text_width(const char *s) {
    if (!s) return 0;
    int w = 0;
    while (*s) { w += FONT_W; s++; }
    return w;
}

/* -- Hardware cursor helpers (bypass double buffer) --------------------- */

void fb_putpixel_hw(int32_t x, int32_t y, uint32_t color) {
    if (!fb.hw_addr) return;
    if (x < 0 || y < 0 || (uint32_t)x >= fb.width || (uint32_t)y >= fb.height)
        return;
    uint32_t *row = (uint32_t*)((uint8_t*)fb.hw_addr + y * fb.pitch);
    row[x] = color;
}

uint32_t fb_getpixel_hw(int32_t x, int32_t y) {
    if (!fb.hw_addr) return 0;
    if (x < 0 || y < 0 || (uint32_t)x >= fb.width || (uint32_t)y >= fb.height)
        return 0;
    uint32_t *row = (uint32_t*)((uint8_t*)fb.hw_addr + y * fb.pitch);
    return row[x];
}

